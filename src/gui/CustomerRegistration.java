    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        
        String mobile = String.valueOf(jTable1.getValueAt(row, 0));
        String fname = String.valueOf(jTable1.getValueAt(row, 1));
        String lname = String.valueOf(jTable1.getValueAt(row, 2));
        String email = String.valueOf(jTable1.getValueAt(row, 3));
        
        jTextField1.setText(mobile);
        jTextField2.setText(fname);
        jTextField3.setText(lname);
        jTextField4.setText(email);
        jTextField1.setEditable(false);
        jButton1.setEnabled(false);
        
        try {
            ResultSet resultSet = MySQL.execute("SELECT COUNT(`id`) FROM `invoice` WHERE `customer_mobile`='" + mobile + "'");
            
            if (resultSet.next()) {
                String count = resultSet.getString(1);
                jTextField5.setText(count);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        if (evt.getClickCount() == 2) {
            if (invoice != null) {
                invoice.getJTextField2().setText(String.valueOf(jTable1.getValueAt(row, 0)));
                invoice.getJLable23().setText(String.valueOf(jTable1.getValueAt(row, 1)) + " " + String.valueOf(jTable1.getValueAt(row, 2)));
                invoice.getjFormattedTextField4().setText(String.valueOf(jTable1.getValueAt(row, 4)));
                this.dispose();
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String mobile = jTextField1.getText();
        String fname = jTextField2.getText();
        String lname = jTextField3.getText();
        String email = jTextField4.getText();
        
        if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter customer first name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter customer last name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter customer email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9\\+_-]+(\\.[A-Za-z0-9\\+_-]+)*@[^-][A-Za-z0-9\\+-]+(\\.[A-Za-z0-9\\+-]+)*(\\.[A-Za-z]{2,})$")) {
            JOptionPane.showMessageDialog(this, "Invalid customer email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet resultset = MySQL.execute("SELECT * FROM `customer` WHERE `email`='" + email + "'");
                
                boolean canUpdate = false;
                
                if (resultset.next()) {
                    
                    if (!resultset.getString("mobile").equals(mobile)) {
                        canUpdate = true;
                    } else {
                        JOptionPane.showMessageDialog(this, "Email already used", "Warning", JOptionPane.WARNING_MESSAGE);
                    }
                    
                } else {
                    canUpdate = true;
                }
                
                if (canUpdate) {
                    MySQL.execute("UPDATE `customer` SET `first_name`='" + fname + "', `last_name`='" + lname + "', `email`='" + email + "' WHERE `mobile`='" + mobile + "'");
                    loadCustomer("first_name", "ASC", "07");
                    reset();
                }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

