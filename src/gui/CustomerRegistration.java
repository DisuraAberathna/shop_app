    private void search() {
        int sortIndex = jComboBox1.getSelectedIndex();
        String mobile = jTextField1.getText();
        
        if (sortIndex == 0) {
            loadCustomer("first_name", "ASC", mobile);
        } else if (sortIndex == 1) {
            loadCustomer("first_name", "DESC", mobile);
        } else if (sortIndex == 2) {
            loadCustomer("points", "ASC", mobile);
        } else if (sortIndex == 3) {
            loadCustomer("points", "DESC", mobile);
        }
    }
    
    private void loadCustomer(String column, String method, String mobile) {
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `customer` WHERE `mobile` LIKE '" + mobile + "%' ORDER BY `" + column + "` " + method + "");
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            
            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("points"));
                
                model.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
